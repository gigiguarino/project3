#include <iostream>
#include <string>
using namespace std;

int main(int argc, char *argv[])
{
  int num = atoi(argv[1]);
  
  if (num == 0)
  {
    cout << "0" << endl;
  }
  
  else if (num == 1)
  {
    cout << "1" << endl;
  }
  
  else if (num == 2)
  {
    cout << "2" << endl;
  }
  
  else if (num == 3)
  {
    cout << "3" << endl;
  }
  
  else if (num == 4)
  {
    cout << "-4" << endl;
  }
  
  else if (num == 5)
  {
    cout << "-3" << endl;
  }
  
  else if (num == 6)
  {
    cout << "-2" << endl;
  }
  
  else if (num == 7)
  {
    cout << "-1" << endl;
  }
  
  return 0;
}
